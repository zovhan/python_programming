x = 7

#penjumlahan
x += 5
print ("penjumlahan: ", x)

#pengurangan
x -= 2
print ("pengurangan: ", x)

#perkalian
x *= 7
print ("perkalian: ", x)

#pembagian
x /= 5
print ("pembagian: ", x)

#modulus
x %= 4
print ("modulus: ", x)

#perpangakatan
x **= 16
print("pangkat: ", x)